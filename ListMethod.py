#append()

subject=['math','geography','physics','algebra']
subject.append('geometry')
print(subject)

print('=====================================')


#extend()

subject=['math','geography','physics','algebra']
more_subject=['drawing','english']

subject.extend(more_subject)
print(subject)

print('=====================================')

#insert()

subject=['math','geography','physics','algebra']
subject.insert(2,'geometry')
print(subject)

print('=====================================')

#remove()

letters=['a','b','c','d']
letters.remove('b')
print(letters)



print('=====================================')

